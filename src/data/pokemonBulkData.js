// Helper function to convert bulk data to full format
export function convertBulkToFull(dexNumber, bulkData) {
  const basic = {
    name: bulkData.name,
    dex_number: dexNumber,
    types: bulkData.types,
    baseStats: generateStatsFromTypes(bulkData.types, dexNumber),
    abilities: generateAbilitiesFromTypes(bulkData.types),
    evolutionLine: generateEvolutionInfo(bulkData),
    movesets: {
      levelUp: generateBasicMovesets(bulkData.types, bulkData.evolveLevel || 50)
    },
    biomes: bulkData.biomes,
    legendary: bulkData.legendary || false,
    spriteUrl: `/sprites/gen3/${String(dexNumber).padStart(3, '0')}.png`
  };
  return basic;
}

// Generate reasonable stats based on type and dex number
function generateStatsFromTypes(types, dexNumber) {
  const baseTotal = dexNumber > 600 ? 520 : dexNumber > 400 ? 480 : dexNumber > 200 ? 450 : 400;
  const variation = Math.floor(Math.random() * 80) - 40; // ±40 variation
  const total = baseTotal + variation;
  
  // Type-based stat biases
  let statBias = { hp: 1, attack: 1, defense: 1, spAttack: 1, spDefense: 1, speed: 1 };
  
  types.forEach(type => {
    switch(type) {
      case 'Fire': statBias.attack *= 1.2; statBias.speed *= 1.1; break;
      case 'Water': statBias.hp *= 1.1; statBias.spAttack *= 1.1; break;
      case 'Grass': statBias.defense *= 1.1; statBias.spDefense *= 1.1; break;
      case 'Electric': statBias.speed *= 1.3; statBias.spAttack *= 1.1; break;
      case 'Psychic': statBias.spAttack *= 1.3; statBias.spDefense *= 1.1; break;
      case 'Fighting': statBias.attack *= 1.3; statBias.hp *= 1.1; break;
      case 'Rock': statBias.defense *= 1.3; statBias.attack *= 1.1; break;
      case 'Steel': statBias.defense *= 1.2; statBias.spDefense *= 1.1; break;
      case 'Dragon': statBias.attack *= 1.2; statBias.spAttack *= 1.2; break;
    }
  });
  
  // Distribute stats with bias
  const totalBias = Object.values(statBias).reduce((a, b) => a + b, 0);
  return {
    hp: Math.floor((total * statBias.hp / totalBias) * 0.9) + 30,
    attack: Math.floor((total * statBias.attack / totalBias) * 0.9) + 30,
    defense: Math.floor((total * statBias.defense / totalBias) * 0.9) + 30,
    spAttack: Math.floor((total * statBias.spAttack / totalBias) * 0.9) + 30,
    spDefense: Math.floor((total * statBias.spDefense / totalBias) * 0.9) + 30,
    speed: Math.floor((total * statBias.speed / totalBias) * 0.9) + 30
  };
}

